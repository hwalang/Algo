#include <iostream>
#include <climits>
#include <algorithm>
#include <cstdlib>

namespace Ans {
#define MAX_N 100

    using namespace std;

    int n;
    int a[MAX_N];

    int Answer() {
        // 입력
        cin >> n;
        for (int i = 0; i < n; i++)
            cin >> a[i];

        // 인접하지 않은 모든 쌍을 다 잡아봅니다.
        int ans = INT_MIN;
        for (int i = 0; i < n; i++)
            // 자연스럽게 중복되는 두 쌍을 제외한다.
            for (int j = i + 2; j < n; j++)
                if (ans < a[i] + a[j])
                    ans = a[i] + a[j];

        cout << ans;

        return 0;
    }
}
